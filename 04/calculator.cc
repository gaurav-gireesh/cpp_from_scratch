/*
* Perform addition of 2 numbers
*/
int add(int num1, int num2)
{
    return num1 + num2;
}

/*
* Perform subtraction of 2 numbers
*/
int subtract(int num1, int num2)
{
    return num1 - num2;
}

/*
* Perform multiplication of 2 numbers
*/
int multiply(int num1, int num2)
{
    return num1 * num2;
}